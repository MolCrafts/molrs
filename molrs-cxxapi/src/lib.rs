//! CXX bridge exposing molrs I/O and analysis to Atomiverse (C++).
//!
//! `AtvMolRec` wraps `molrs::MolRec` directly — no C++ mirror, no dual storage.
//! Precision selected at build time via `f64` Cargo feature.

use std::fs::{File, OpenOptions};
use std::io::{BufWriter, Write};

use molrs::block::Block;
use molrs::frame::Frame;
use molrs::io::xyz::write_xyz_frame;
use molrs::molrec::MolRec;
use molrs::region::simbox::SimBox;
use molrs::types::F;
use ndarray::{Array1, Array2, ArrayD};

